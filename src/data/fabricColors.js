export const FABRIC_BRANDS = [KONA_COTTON, BELLA_SOLIDS, PURE_ELEMENTS, CONFETTI_COTTONS];

export function getAllColorsForBrand(brand) {
  return Object.values(brand.families).flatMap(f => f.colors);
}

export function getAllColors() {
  return FABRIC_BRANDS.flatMap(brand =>
    getAllColorsForBrand(brand).map(color => ({
      ...color, brandId: brand.id, brandName: brand.brand, lineName: brand.line,
    }))
  );
}

export function findClosestColors(targetHex, count = 5, brandId = null) {
  const target = hexToRgb(targetHex);
  if (!target) return [];
  let pool = getAllColors();
  if (brandId) pool = pool.filter(c => c.brandId === brandId);
  return pool
    .map(color => ({ ...color, distance: colorDistance(target, hexToRgb(color.hex)) }))
    .sort((a, b) => a.distance - b.distance)
    .slice(0, count);
}

export function getBrandById(brandId) {
  return FABRIC_BRANDS.find(b => b.id === brandId) || null;
}

export function hexToRgb(hex) {
  const c = (hex || '').replace('#', '');
  if (c.length !== 6) return null;
  return {
    r: parseInt(c.slice(0, 2), 16),
    g: parseInt(c.slice(2, 4), 16),
    b: parseInt(c.slice(4, 6), 16),
  };
}

export function colorDistance(a, b) {
  if (!a || !b) return Infinity;
  return Math.sqrt((a.r - b.r) ** 2 + (a.g - b.g) ** 2 + (a.b - b.b) ** 2);
}
